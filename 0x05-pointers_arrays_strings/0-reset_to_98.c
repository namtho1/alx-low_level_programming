/**
 * reset_to_98 - Updates the value of the integer to 98.
 * @n: Pointer to the integer whose value needs to be updated.
 *
 * Description: This function takes a pointer to an int as a parameter
 * and updates the value it points to with the value 98.
 */
void reset_to_98(int *n)
{
    *n = 98;
}
